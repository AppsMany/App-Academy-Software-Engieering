// (Logical AND $$)

console.log(false && true );  //prints out false
console.log(false && false);  //prints out false
console.log (true && false);  //prints out false
console.log(true && true);    //prints out true


// (Logical OR || ) 

console.log(false || true );  //prints out true
console.log(true || false );  //prints out true
